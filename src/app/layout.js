import { Inter } from "next/font/google";
import 'bootstrap/dist/css/bootstrap.min.css';
import 'overlayscrollbars/styles/overlayscrollbars.css';
import "./globals.scss";
import ReduxProvider from "@/components/ReduxProvider";
import { StyledComponentRegistry } from "@/libs/AntRegistry";
import AccountProvider from "@/components/AccountProvider";
import { cookies } from "next/headers";
import ScrollbarComponent from "@/components/ScrollbarComponent";
const inter = Inter({ subsets: ["latin"] });

export const metadata = {
  title: "Vinhuni Educator",
  description: "Generated by Vinhuni Educator",
};

export default function RootLayout({ children }) {
  const cookieStore = cookies();
  const accessToken = cookieStore.get('accessToken')
  return (
    <html lang="en">
      <body className={inter.className}>
      <ScrollbarComponent>
        <ReduxProvider>
          <StyledComponentRegistry>
            <AccountProvider accessToken={accessToken?.value}>
              {children}
            </AccountProvider>
          </StyledComponentRegistry>
        </ReduxProvider>
      </ScrollbarComponent>
      </body>
    </html>
  );
}
